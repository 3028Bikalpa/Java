public class forloop {
    public static void main(String[] args) {
        System.out.println("List of first 100 natural numbers");
        for (int i = 1; i < 101; i++) {
            System.out.println(i);
        }
        System.out.println("To print first 10 odd numbers");
        for (int i = 0; i < 10; i++) {
            System.out.println((2*i)+1);
        }
    }
}
